a = float(9.99)
print("FLOAT TO FLOAT a is " ,a)		#SIMILAR TO A=9.99

a = float(100)
print("INT TO FLOAT a is " ,a)	

a = float("9.99")
print("FLOAT STRING TO FLOAT a is " ,a)

a = float("9")
print("INT STRING TO FLOAT a is " ,a)

a = float("1.00E4")
print("SCIENTIFIC NOTATION TO FLOAT a is " ,a)

a = float("1.00E-4")
print("SCIENTIFIC NOTATION TO FLOAT a is " ,a)

