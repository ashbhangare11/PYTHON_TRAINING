a = str(10)
print("INT TO STRING: a is " ,a)		# 10

a=str(11.10)
print("FLOAT TO STRING: a is " ,a)		# 11.1

a = str(2/5)
print("FLOAT EXPRESSION TO STRING: a is " ,a)	# 0.4

a=str(10E4)
print("SCIENTIFIC NOTATION TO STRING: a is " ,a)# 100000.0

a=str(1.23e-4)
print("SCIENTIFIC NOTATION TO STRING: a is " ,a)# 0.000123

a=str('True')
print("BOOLEAN TO STRING: a is " ,a)		# True

a=str([1,2,3])
print("LIST TO STRING: a is " ,a)		# [1, 2, 3]

a=str((1,2,3))
print("TUPLE TO STRING: a is " ,a)		# (1, 2, 3)

a=str({1:100, 2:200, 3:300})
print("DICTIONARY TO STRING: a is " ,a)		# {1: 100, 2: 200, 3: 300}




