# Getting type of values
print(type(123))
print(type(9.99))

# Getting type of variables
a = 10
b = 2.12
c = "Hello"
d = (10, 20, 30)
e = [10, 20, 30]

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))

# Declaring a variable And assigning an value
x = 10				#INTEGER VALUE
print("x = ", x)
print("type of x = ", type(x))

x = "Hello World!"		#STRING VALUE
print("x = ", x)
print("type of x = ", type(x))